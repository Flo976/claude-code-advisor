#!/usr/bin/env python3
"""
Claude Code Advisor — Local setup scanner.

Refreshes `references/skills-catalog.md` by scanning ~/.claude/skills,
~/.claude/plugins, and MCP config files. Fast, no network, no LLM.

Full knowledge base updates (web research + reference file rewrites) are
handled by the `/advisor update` flow in SKILL.md, which dispatches
subagents in parallel.
"""

import json
import sys
from pathlib import Path
from datetime import datetime

ADVISOR_DIR = Path(__file__).parent.parent
REFERENCES_DIR = ADVISOR_DIR / "references"

CLAUDE_SKILLS_DIR = Path.home() / ".claude" / "skills"
CLAUDE_PLUGINS_DIR = Path.home() / ".claude" / "plugins"


def scan_local_setup():
    skills = []
    if CLAUDE_SKILLS_DIR.exists():
        for skill_dir in sorted(CLAUDE_SKILLS_DIR.iterdir()):
            skill_md = skill_dir / "SKILL.md"
            if skill_md.exists():
                content = skill_md.read_text(encoding="utf-8")
                skills.append({
                    "name": skill_dir.name,
                    "description": _extract_yaml_description(content)[:200],
                })

    mcps = []
    for mcp_file in [Path.home() / ".claude" / ".mcp.json", Path.cwd() / ".mcp.json"]:
        if mcp_file.exists():
            try:
                data = json.loads(mcp_file.read_text())
                mcps.extend(data.get("mcpServers", {}).keys())
            except (json.JSONDecodeError, KeyError):
                pass

    plugins = []
    if CLAUDE_PLUGINS_DIR.exists():
        for marketplace in CLAUDE_PLUGINS_DIR.glob("marketplaces/*/plugins/*"):
            if marketplace.is_dir():
                plugins.append(marketplace.name)

    catalog_path = REFERENCES_DIR / "skills-catalog.md"
    lines = [
        "# Skills Catalog (auto-generated)\n",
        f"\n> Last updated: {datetime.now().strftime('%Y-%m-%d %H:%M')}\n",
        "> This file is auto-generated by update-knowledge.py and is .gitignored.\n",
        f"\n## Installed Skills ({len(skills)})\n\n",
        "| Skill | Description |\n",
        "|-------|-------------|\n",
    ]
    for s in skills:
        lines.append(f"| `{s['name']}` | {s['description']} |\n")

    lines.append(f"\n## Configured MCPs ({len(mcps)})\n\n")
    for m in mcps:
        lines.append(f"- `{m}`\n")

    lines.append(f"\n## Installed Plugins ({len(plugins)})\n\n")
    for p in plugins:
        lines.append(f"- `{p}`\n")

    catalog_path.write_text("".join(lines), encoding="utf-8")
    return {"skills": len(skills), "mcps": len(mcps), "plugins": len(plugins)}


def _extract_yaml_description(content: str) -> str:
    lines = content.split("\n")
    in_frontmatter = False
    in_description = False
    desc_lines = []

    for line in lines:
        if line.strip() == "---":
            if in_frontmatter:
                break
            in_frontmatter = True
            continue

        if not in_frontmatter:
            continue

        if line.strip().startswith("description:"):
            value = line.split("description:", 1)[1].strip()
            if value and value not in ("|", ">", ">-", "|-"):
                return value.strip("\"'")
            in_description = True
            continue

        if in_description:
            if line and not line[0].isspace() and ":" in line:
                break
            if line.strip():
                desc_lines.append(line.strip())

    return " ".join(desc_lines)


def main():
    quiet = "--quiet" in sys.argv
    stats = scan_local_setup()
    if not quiet:
        print(
            f"skills-catalog.md refreshed: "
            f"{stats['skills']} skills, {stats['mcps']} MCPs, {stats['plugins']} plugins"
        )


if __name__ == "__main__":
    main()
